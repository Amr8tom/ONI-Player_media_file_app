import 'package:flutter/services.dart';
import 'package:oniplayer/domain/models/stream.dart';

abstract class SqlStorageRepository {
  /* -------------------------------------------------------------------------- */
  /*                          INSERT thumbnail to cache                         */
  /* -------------------------------------------------------------------------- */
  Future<bool> setThumbnailCache(String path, Uint8List image);

  /* -------------------------------------------------------------------------- */
  /*                         QUERY thumbnail from cache                         */
  /* -------------------------------------------------------------------------- */
  Future<Uint8List?> getThumbnailCache(String path);

  /* -------------------------------------------------------------------------- */
  /*                             INSERT to played list                             */
  /* -------------------------------------------------------------------------- */
  Future<bool> addToPlayedList(String path);

  /* -------------------------------------------------------------------------- */
  /*                               query played list                              */
  /* -------------------------------------------------------------------------- */
  Future<List<String>> getPlayedList();

  /* -------------------------------------------------------------------------- */
  /*                               INSERT settings                              */
  /* -------------------------------------------------------------------------- */
  Future<bool> setSetting(String key, String value);

  /* -------------------------------------------------------------------------- */
  /*                               QUERY settings                               */
  /* -------------------------------------------------------------------------- */
  Future<String?> getSetting(String key);

  /* -------------------------------------------------------------------------- */
  /*                               INSERT streams                               */
  /* -------------------------------------------------------------------------- */

  Future<bool> addStream(StreamModel stream);
  /* -------------------------------------------------------------------------- */
  /*                                QUERY streams                               */
  /* -------------------------------------------------------------------------- */
  Future<List<StreamModel>> getStreams();

  /* -------------------------------------------------------------------------- */
  /*                                DELETE stream                               */
  /* -------------------------------------------------------------------------- */
  Future<bool> deleteStream(StreamModel stream);

  /* -------------------------------------------------------------------------- */
  /*                                UPDATE stream                               */
  /* -------------------------------------------------------------------------- */
  Future<bool> updateStream(StreamModel oldOne, StreamModel newOne);
}
